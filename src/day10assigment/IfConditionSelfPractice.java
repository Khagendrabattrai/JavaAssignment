package day10assigment;

public class IfConditionSelfPractice {

	public static void main(String[] args) {
		/*
		 * write a program to relate two integers  using = =or > or <
		 * sign
		 */
		int a = 9;
		int b = 5;
		if (a == b) {
			System.out.println("a is equals b.");// false
		}

		if (a > b) {
			System.out.println("a is greater than b.");// true
		}
		if (a < b) {
			System.out.println("a is less than b.");// true
		}
	}
}
